# Tuples

my_tuple = (1, 2, 3, 4, 5)

print(my_tuple)
print(my_tuple[0])    #using indexing to print the first parameter
print(my_tuple[2])    #print the third parameter
print(my_tuple[-2])

tuple1 = (1, 2, 3)
tuple2 = (4, 5, 6)

conc_tuple = tuple1 + tuple2   #concatinating two tuples together
print(conc_tuple)

rep_tuple = tuple1 * 3     #repitition of a tuple 
print(rep_tuple)